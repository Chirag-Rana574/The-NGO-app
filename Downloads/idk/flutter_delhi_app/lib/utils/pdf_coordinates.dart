// PDF Field Coordinates
// Ported from the web app's TypeScript exporters.
// Each form maps field names to their exact (x, yFromTop, maxWidth, page, fontSize) positions.
// Syncfusion uses a top-left origin, so yFromTop translates directly as the Y axis.

class FieldCoord {
  final int page;
  final double x;
  final double yFromTop;
  final double maxWidth;
  final double fontSize;
  final bool whiteout;

  const FieldCoord({
    this.page = 0,
    required this.x,
    required this.yFromTop,
    required this.maxWidth,
    this.fontSize = 11,
    this.whiteout = false,
  });
}

